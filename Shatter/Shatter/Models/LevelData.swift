import Foundation

struct LevelData: Codable {
    let id: Int
    let name: String
    let blocks: [BlockData]

    // デフォルトレベル生成用
    static func defaultLevel(id: Int) -> LevelData {
        var blocks: [BlockData] = []
        let patterns = LevelPatterns.pattern(for: id)
        for (row, cols) in patterns.enumerated() {
            for (col, type) in cols.enumerated() {
                if type > 0 {
                    blocks.append(BlockData(row: row, col: col, type: type))
                }
            }
        }
        return LevelData(id: id, name: "STAGE \(String(format: "%02d", id))", blocks: blocks)
    }
}

